CREATE TABLE [dbo].[Surveys]
(
	[Id] INT IDENTITY(1, 1) NOT NULL, 
    [Name] NVARCHAR(128) NOT NULL, 
    [Audience] NVARCHAR(50) NOT NULL,
	[UserId] INT NOT NULL, 

    CONSTRAINT [PK_Surveys] PRIMARY KEY CLUSTERED ([Id] ASC),
	CONSTRAINT [FK_Surveys_UserProfile] FOREIGN KEY (UserId) REFERENCES [UserProfile](Id) ON DELETE CASCADE,
)
